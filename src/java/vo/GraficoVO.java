
package vo;

public class GraficoVO {
	private int id;
	private String tipo_chamado;
	private int qtd_chamado;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTipo_chamado() {
		return tipo_chamado;
	}
	public void setTipo_chamado(String tipo_chamado) {
		this.tipo_chamado = tipo_chamado;
	}
	public int getQtd_chamado() {
		return qtd_chamado;
	}
	public void setQtd_chamado(int qtd_chamado) {
		this.qtd_chamado = qtd_chamado;
	}
}
